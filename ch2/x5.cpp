#include <iostream>
#include <string>

int main()
{
	bool a = true;
	std::cout << "a bool: " << a << '\n';

	char b = 'a';
	std::cout << "a char: " << b << '\n';

	int c = 42;
	std::cout << "an int: " << c << '\n';

	double d = 2.714;
	std::cout << "a double: " << d << '\n';

	std::string e = "forty-two";
	std::cout << "a string: " << e << '\n';
}
